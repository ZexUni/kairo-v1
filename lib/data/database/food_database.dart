  static List<FoodModel> forState(String physiologicalState) {
    // physiologicalState: anabolic, metabolic, stress, recovery, fat_loss
    return foods.where((f) => f.suitableStates.contains(physiologicalState.toLowerCase())).toList();
  }

  static List<FoodModel> forGoal(String goal) {
    // bulk -> P3/P2/P1, cut -> P1/P4/P5, recomp/maintenance -> all
    if (goal == 'bulk') {
      return foods.where((f) => f.macroPreset == 'P3' || f.macroPreset == 'P2' || f.macroPreset == 'P1').toList();
    } else if (goal == 'cut') {
      return foods.where((f) => f.macroPreset == 'P1' || f.macroPreset == 'P4' || f.macroPreset == 'P5').toList();
    } else {
      return foods;
    }
  }

  static List<FoodModel> vegetarianOnly() {
    return foods.where((f) => f.isVegetarian).toList();
  }

  static List<FoodModel> byCategory(String category) {
    return foods.where((f) => f.category.toLowerCase() == category.toLowerCase()).toList();
  }
