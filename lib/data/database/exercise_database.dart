  static List<ExerciseModel> forEquipment(String mode) {
    // mode: bodyweight, homeGym, fullGym
    // mapping to equipmentType:
    // bodyweight -> ['bodyweight']
    // homeGym -> ['bodyweight', 'home_gym']
    // fullGym -> ['bodyweight', 'home_gym', 'full_gym']
    if (mode == 'bodyweight') {
      return exercises.where((e) => e.equipmentType == 'bodyweight').toList();
    } else if (mode == 'homeGym') {
      return exercises.where((e) => e.equipmentType == 'bodyweight' || e.equipmentType == 'home_gym').toList();
    } else {
      return exercises;
    }
  }

  static List<ExerciseModel> forMuscleGroup(String group) {
    return exercises.where((e) => e.muscleGroup.toLowerCase() == group.toLowerCase()).toList();
  }

  static List<ExerciseModel> forMuscleAndEquipment(String muscle, String mode) {
    final eqList = forEquipment(mode);
    return eqList.where((e) => e.muscleGroup.toLowerCase() == muscle.toLowerCase() || e.primaryMuscle.toLowerCase() == muscle.toLowerCase()).toList();
  }
